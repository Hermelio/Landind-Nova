var idioma = function() {
    $('.box-flag').click(function() {
        $('.content-flag').slideToggle('fast');
        $(this).find('.btn-select-flag').toggleClass('btn-select-flag--open');
    });
    $('.content-flag .box-flag__text').click(function(){
        var trocar = $(this).text();
        var icon = $(this).attr('data-id');
        var change = icon;
        $('.flag-select .box-flag__text').text(trocar);
        var icon = $('.flag-select .box-flag__text').attr('data-id', icon);
        $('.content-flag .box-flag__text').removeClass('x-hide'); // removendo a classe e colocando ela de novo em outra posição 
        $(this).addClass('x-hide');
        var change = $('.bolha .bolha--change, .bolha .bolha--change1').attr('data-id',change);
    });
};
 var pintar = function(){
    $('.saldo-kit dt').click(function(){
        $(this).toggleClass('novaazul');
        $(this).next().slideToggle('fast');
    });
 };
var traduzir = function(){
    $('.content-flag .box-flag__text').click(function(){
        var translate = $(this).attr('data-id').split('-')[1];
        $('body').translate({lang: translate, t: tradutor});
    });
};
$(document).ready(function() {
    carrossel();
    idioma();
    pintar();   
    traduzir();
 });